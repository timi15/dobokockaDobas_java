     private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
          

          int randomSz = sorsolas();
          System.out.println(randomSz);
          
          if((jRadioButton1.isSelected() && randomSz==1) || (jRadioButton2.isSelected() && randomSz==2 )|| (jRadioButton3.isSelected() && randomSz==3 )  || 
                  (jRadioButton4.isSelected() && randomSz==4) ||  (jRadioButton5.isSelected() && randomSz==5) || (jRadioButton6.isSelected() && randomSz==6)){
              
               toke+= (jSlider1.getValue())*6;
               jLabel9.setText("Nyertél!");
               jLabel9.setForeground(Color.green);
               
               
          } else{
               toke-= jSlider1.getValue();
                jLabel9.setText("Vesztettél!");
                jLabel9.setForeground(Color.red);
          }
          
          
          jLabel4.setText("$"+toke);
          jLabel6.setText(""+toke);
          jSlider1.setMaximum(toke);
          
          if(toke==0){
               JOptionPane.showMessageDialog(null, "Elfogyotta pénzed", "Figyelem!", JOptionPane.ERROR_MESSAGE);
                System.exit(0);
          }
  
          
          
          for (int i = 0; i < kepek.length; i++) {
               if(randomSz==i+1){
                     jLabel8.setIcon(new javax.swing.ImageIcon(getClass().getResource(kepek[i]))); // NOI18N
                    
               }
               
          }
          
          
     }//GEN-LAST:event_jButton3ActionPerformed

