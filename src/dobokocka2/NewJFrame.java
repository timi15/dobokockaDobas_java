     private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
          String ertek = jTextField1.getText();
          try {
               toke = Integer.parseInt(ertek);
          } catch (NumberFormatException e) {
               JOptionPane.showConfirmDialog(null, "Hibás értéket adtál meg", "Figyelem!", JOptionPane.WARNING_MESSAGE);
          }

          if (toke <= 0) {
               toke = 1000;
               JOptionPane.showMessageDialog(null, "A t?két $1000-ra állítottam be", "Figyelem!", JOptionPane.INFORMATION_MESSAGE);
          }

          jTextField1.setVisible(false);
          jButton2.setVisible(false);
          jPanel2.setVisible(true);
          jLabel2.setText("$" + toke);

          jSlider1.setMaximum(toke);

     }//GEN-LAST:event_jButton2ActionPerformed

